class Solution {
  public:
    int getTotalNodes(Node* tree){
        if(tree==NULL){
            return 0;
        }else{
            return 1 + getTotalNodes(tree->left) + getTotalNodes(tree->right);
        }
    }
    bool isHeap(Node* tree, int rootIndex, int &totalNodes, int val){
        if(tree==NULL){
            return true;
        }else if(tree->data > val){
            return false;
        }else if(rootIndex >= totalNodes){
            return false;
        }else{
            return isHeap(tree->left, 2*rootIndex+1, totalNodes, tree->data) && isHeap(tree->right, 2*rootIndex+2, totalNodes, tree->data);
        }
    }
    bool isHeap(struct Node* tree) {
        int totalNodes = getTotalNodes(tree);
        return isHeap(tree, 0, totalNodes, tree->data);
    }
};

