class Solution
{
public:
    
    int sumOfLongRootToLeafPath(Node *root)
    {
        int ans = 0, currH = 0;
        queue<pair<Node*,pair<int,int>>> q;
        if(root)
            q.push({root,{root->data,0}});
        
        while(!q.empty()){
            Node *node = q.front().first;
            int sum = q.front().second.first;
            int h = q.front().second.second;
            q.pop();
            if(currH<h){
                ans = sum;
                currH = h;
            }else if(currH==h){
                ans = max(ans,sum);
            }
            if(node->left)
                q.push({node->left,{sum+node->left->data,h+1}});
            if(node->right)
                q.push({node->right,{sum+node->right->data,h+1}});
        }
        return ans;
    }
};

