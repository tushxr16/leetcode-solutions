Node *flatten(Node *root)
{
   Node *ptr = root, *ans=nullptr;
   vector<int> v;
   while(ptr){
       Node *tmp = ptr->bottom, *tmp2 = new Node(ptr->data);
       tmp2->bottom=ans;
       ans=tmp2;
       while(tmp){
        tmp2 = new Node(tmp->data);
        tmp2->bottom=ans;
        ans=tmp2;
        tmp=tmp->bottom;
       }
       ptr=ptr->next;
   }
   Node *ptr2=nullptr;
   ptr = ans;
   while(ptr){
       ptr2 = ptr->bottom;
       while(ptr2){
           if(ptr2->data < ptr->data)
            swap(ptr2->data,ptr->data);
            ptr2=ptr2->bottom;
       }
       ptr=ptr->bottom;
   }
   return ans;
}

