class Solution
{
    public:
    //Function to delete middle element of a stack.
    void deleteMid(stack<int>&s, int sizeOfStack)
    {
        vector<int> v;
        double n = (sizeOfStack+1);
        n/=2;
        n = ceil(n);
        for(int i=0;i<n;i++){
            v.push_back(s.top());
            s.pop();
        }
        v.pop_back();
        while(v.size()){
            s.push(v[v.size()-1]);
            v.pop_back();
        }
        return;
    }
};

